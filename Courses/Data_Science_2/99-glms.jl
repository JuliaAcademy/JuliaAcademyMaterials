# # Generalized Linear Models
#
# Generalized Linear Models (GLMs) are a family of statistical models where the prediction
# is based on a function of a linear predictor $x_^T\beta$ where $x$ is a vector of features
# and $\beta$ is the parameter vector.  
#
# One of the big assumptions of GLMs is that there is a strictly increasing *link function*
# $g$ that "links" the mean of the distribution $E(Y|X) = \mu$ to the linear predictor 
# $x^T\beta$.  In other words, our prediction of $y$ given $x$ is $g^{-1}(x^T\beta)$.




# The two most common GLMs are linear and logistic regression.

